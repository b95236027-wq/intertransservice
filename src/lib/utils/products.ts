export function getAllProducts(): Product[] {
	return Object.entries(productsData).map(([slug, data]) => ({
		...data,
		slug
	}));
}

export function getProductBySlug(slug: string): Product | null {
	const product = productsData[slug];
	if (!product) return null;
	return {
		...product,
		slug
	};
}

